#En Python, los "parámetros formales" son los nombres de variables utilizados en la definición de una función, mientras que los "parámetros reales" o "argumentos" son los valores concretos que se pasan a la función cuando se llama. Por ejemplo:

def suma(a, b):  # 'a' y 'b' son parámetros formales
    resultado = a + b
    return resultado

resultado = suma(5, 3)  # 5 y 3 son parámetros reales